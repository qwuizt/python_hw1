quarter = int(input("Введите номер четверти (1,2,3,4): "))
if quarter == 1:
    print("X:(0;+∞) Y:(0;+∞)")
elif quarter == 2:
    print("X:(-∞;0) Y:(0;+∞)")
elif quarter == 3:
    print("X:(-∞;0) Y:(-∞;0)")
elif quarter == 4:
    print("X:(0;+∞) Y:(-∞:0)")
else:
    print("Введите номер четверти правильно")